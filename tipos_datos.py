# Ejemplo de tipos de datos en python

#Entero
edad = 32
print("Edad: " + str(edad))

# Numero con punto flotante
altura = 1.80
print("Altura: " + str(altura))

# Cadena de Texto
nombre = "David"
print("Nombre: " + str(nombre))

# Tipo boolean
es_estudiante = False
print("Estudiante: " + str(es_estudiante))

# Variable tipo None, asensia de valor
direccion = None
print("Direccion: " + str(direccion))