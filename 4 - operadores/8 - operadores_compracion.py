print('**** OPERADORES DE COMPARACION ****')

a, b = 7,5
print(f'El valor de a = {a}')
print(f'El valor de b = {b}')

# Operador de igualdad ==
resultado = (a==b)
print(f'El resultado de a==b es {resultado}')

# Operador diferente !=
resultado = (a!=b)
print(f'El resultado de a!=b es {resultado}')

# Operador mayor que >
resultado = (a>b)
print(f'El resultado de a>b es {resultado}')

# Operador moyor o igual que >=
resultado = (a>=b)
print(f'El resultado de a>=b es {resultado}')

# operador de menor que <
resultado = (a<b)
print(f'El resultado de a<b es {resultado}')

# operador de manor o igual que <=
resultado = (a<=b)
print(f'El resultado de a<=b es {resultado}')