# Operadores aritmeticos

a = 10
b = 3

# suma
suma = a + b
print(f'La suma es: {suma}')

# resta
resta = a - b
print(f'La resta es: {resta}')

# multiplicacion
multiplicacion = a * b
print(f'La multiplicacion es: {multiplicacion}')

# division
division = a / b
print(f'La division es: {division:.2f}')

# division entero
division_entero = a // b
print(f'La division entero es: {division_entero}')

# residuo de la division
residuo = a % b
print(f'La residuo es: {residuo}')

# exponente
exponencial = a ** b
print(f'La exponencial es: {exponencial}')


