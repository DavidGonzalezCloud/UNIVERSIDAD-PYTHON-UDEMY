print('**** OPERADORES DE ASIGNACION COMPUESTOS')

a,b = 20,15
print(f'\nEl valor de a = {a}')
print(f'El valor de b = {b}')

# operador compuesto de suma +=
a += b # a = a + b
print(f'\nEl valor de a = {a}')

# operador compuesto de resta -=
a = 20
a -= b # a = a-b
print(f'\nEl valor de a = {a}')

a = 20
a *= b # a = a*b
print(f'\nEl valor de a = {a}')

a = 20
a /= b # a = a/b
print(f'\nEl valor de a = {a}')