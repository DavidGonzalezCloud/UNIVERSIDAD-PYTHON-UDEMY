# Variables en Python

# declaracion e inicializacion de variables

edad = 32
altura = 1.80
pais = "El Salvador"


# Acceder a variables
print("Edad: " + str(edad))
print("Altura: " + str(altura) + "m")
print("Pais: " + str(pais))
print(edad)

# Modificar el valor de una variable
edad = 30
altura = 1.60
pais = "USA"

print("Edad: " + str(edad))
print("Altura: " + str(altura) + "m")
print("Pais: " + str(pais))

# En python las variables son de tipo dinamicas
edad = "treinta"
print("Edad: " + str(edad))