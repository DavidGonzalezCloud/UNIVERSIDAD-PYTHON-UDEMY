# Metodos de cadenas

cadena1 = 'Hola Mundo'
print(f'cadena original: {cadena1}')

mayusculas = cadena1.upper()
print(f'mayusculas: {mayusculas}')

minusculas = cadena1.lower()
print(f'minusculas: {minusculas}')

# mayusculas sin usar nueva variable
print(f'cadena en mayusculas: {cadena1.upper()}')

cadena2 = ' David Gonzalez '
print(f'Cadena con espacios: {cadena2}')
print(f'cadena sin espacios: {cadena2.strip()}')

